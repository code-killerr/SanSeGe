
//鼠标点击 加号变减号 下拉列表出现  再次点击 减号变加号 下拉列表消失
function hid1(){
	if(document.getElementById("hid1").className=="fa fa-plus"){
		document.getElementById("hid1").className="fa fa-minus"
		document.getElementById("bunderlist1").style.display="block"
	}
	else{
		document.getElementById("hid1").className="fa fa-plus"
		document.getElementById("bunderlist1").style.display="none"
	}
}
