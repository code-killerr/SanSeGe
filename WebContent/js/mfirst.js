//点赞专栏
function dislike(){
	if(document.getElementById("Ydislike").className=="fa fa-thumbs-down"){
		document.getElementById("Ydislike").className="fa fa-thumbs-o-down";
	}
	else{
		document.getElementById("Ydislike").className="fa fa-thumbs-down"
	}
}
function like(){
	if(document.getElementById("Ylike").className=="fa fa-thumbs-up"){
		document.getElementById("Ylike").className="fa fa-thumbs-o-up";
	}
	else{
		document.getElementById("Ylike").className="fa fa-thumbs-up"
	}
}
